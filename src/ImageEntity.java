/**ImageEntity
 * - extends Entity class
 * - adds a new image to the game
 */
class ImageEntity extends Entity {

    /**constructor
     * @param r - image location reference
     * @param x - image x coordinate
     * @param y - image y coordinate
     */
    ImageEntity(String r, int x, int y) {
        super(r, x, y);
    } // ImageEntity
} // ImageEntity class